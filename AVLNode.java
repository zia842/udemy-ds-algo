package com.ds.algo;

public class AVLNode {
	
	public int data;
	
	public int height;
	
	public AVLNode leftChild;
	
	public AVLNode rightChild;
	
	public AVLNode(int data, int height, AVLNode leftChild, AVLNode rightChild) {
		super();
		this.data = data;
		this.height = height;
		this.leftChild = leftChild;
		this.rightChild = rightChild;
	}


	

}
